        public void SpritesUpdate()
        {
            if (_spritesEnemy.X < _limitSprites.X - 1)
                _spritesEnemy.X += 1;
            else
                _spritesEnemy.X = 0;
            _timeChangeSprite = 0;
        }

        public void Update(GameTime gameTime)
        {
            //Animation
            _timeChangeSprite += (float)gameTime.ElapsedGameTime.TotalSeconds;

            if (_timeChangeSprite > 0.2)
            {
                SpritesUpdate();
            }
        }


